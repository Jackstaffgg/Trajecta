export function formatDateByLocale(iso: string, locale: Locale): string {
  const date = new Date(iso);
  if (Number.isNaN(date.getTime())) {
    return locale === "ru" ? "только что" : locale === "uk" ? "щойно" : "just now";
  }
  return date.toLocaleString(locale === "ru" ? "ru-RU" : locale === "uk" ? "uk-UA" : "en-US");
}










